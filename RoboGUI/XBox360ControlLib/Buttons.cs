namespace XBox360ControlLib
{
    /// <summary>
    /// List of all Buttons on the xBox 360 controller.
    /// </summary>
    public enum Buttons
    {
        /// <summary>
        /// The d pad up
        /// </summary>
        DPadUp,

        /// <summary>
        /// The d pad down
        /// </summary>
        DPadDown,

        /// <summary>
        /// The d pad left
        /// </summary>
        DPadLeft,

        /// <summary>
        /// The d pad right
        /// </summary>
        DPadRight,

        /// <summary>
        /// The left bumper
        /// </summary>
        LeftBumper,

        /// <summary>
        /// The right bumper
        /// </summary>
        RightBumper,

        /// <summary>
        /// The back
        /// </summary>
        Back,

        /// <summary>
        /// The start
        /// </summary>
        Start,

        /// <summary>
        /// The a button
        /// </summary>
        AButton,

        /// <summary>
        /// The b button
        /// </summary>
        BButton,

        /// <summary>
        /// The x button
        /// </summary>
        XButton,

        /// <summary>
        /// The y button
        /// </summary>
        YButton,

        /// <summary>
        /// The left stick
        /// </summary>
        LeftStick,

        /// <summary>
        /// The right stick
        /// </summary>
        RightStick,

        /// <summary>
        /// The big button
        /// </summary>
        BigButton
    }
}